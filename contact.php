	<script type="text/javascript">
		//message us control
		name_val = 0;
		email_val = 0;
		msg_val = 0;
		
		$('#name').keyup(function()
		{
			var name= $('#name').val();
			if(name!='')
			{
				name_val =1;
			}
			else
			{
				name_val = 0;
			}
		});
		
		$('#email').keyup(function()
		{
			var email= $('#email').val();
			if(email!='')
			{
				$.post('email.php',{email: email},function(e)
				{
					//$('#email_feed').html(e);
					
					
					if(e=="1")
					{
						$('#email_feed').html('valid email address');
						email_val = 1;
					}
					else
					{
						$('#email_feed').html('invalid email address');
						email_val = 0;
					}
					
				});
			}
			else
			{
				$('#email_feed').html('');
				email_val = 0;
			}
		});
		
		
		$('#msg').keyup(function()
		{
			var msg = $('#msg').val();
			if(msg!='')
			{
				msg_val =1;
			}
			else
			{
				msg_val = 0;
			}
		});
		
		
		$('#send').click(function()
		{
			if(name_val==1 && email_val==1 && msg_val==1)
			{
				$('#msg_us').fadeOut(500);
				$('#msg_feed').fadeIn(500).html('please wait...');
				
				var name= $('#name').val();
				var email= $('#email').val();
				var msg= $('#msg').val();
			
				$.post('mailing.php', {name: name, email: email, msg: msg}, function()
				{
					//$('#msg_feed').fadeIn(500).html(data);
					$('#msg_feed').hide().fadeIn(1000).html("Thanks " + name + ", for contacting us.<br>We will respond to you soon. ");
				});
				
			}
			else
			{
				$('#msg_feed').fadeIn(500).html('Kindly fill all the fields correctly.');
			}
		});
		
	</script>
	
